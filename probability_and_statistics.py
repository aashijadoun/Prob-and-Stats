import pandas as pd
import matplotlib.pyplot as plt

# Load the historical data
data = pd.read_csv('/content/Lottery_Mega_Millions_Winning_Numbers__Beginning_2002.csv')

# Convert date column to datetime format
data['Draw Date'] = pd.to_datetime(data['Draw Date'])

# Extract winning numbers and year from the date
data['Year'] = data['Draw Date'].dt.year
data['Winning Numbers'] = data['Winning Numbers']  # Replace 'winning_number_column' with your column name

# Group data by year and analyze frequency trends over time
number_frequency_yearly = data.groupby(['Year', 'Winning Numbers']).size().unstack().fillna(0)

# Plotting frequency of each number over the years
plt.subplots(figsize=(12, 8))
number_frequency_yearly.plot(marker='o')
plt.title('Frequency of Winning Numbers Over Years')
plt.xlabel('Year')
plt.ylabel('Frequency')
plt.legend(title='Number', bbox_to_anchor=(1.05, 1), loc='upper left')
plt.subplots_adjust(top=0.92, bottom=0.08, left=0.10, right=0.90, hspace=0.25, wspace=0.35)
# Adjust top, bottom, left, right, hspace, wspace to fit the plot
plt.show()

