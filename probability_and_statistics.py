import pandas as pd
import seaborn as sns
import matplotlib.pyplot as plt

# Load the historical data
data = pd.read_csv('/content/Lottery_Mega_Millions_Winning_Numbers__Beginning_2002.csv')

# Extract the columns containing winning numbers (adjust columns as per your dataset)
winning_numbers = data[['Draw Date', 'Winning Numbers', 'Mega Ball', 'Multiplier']]

# Calculate the correlation matrix
correlation_matrix = winning_numbers.corr(numeric_only=True)

# Plotting the correlation matrix using a heatmap
plt.figure(figsize=(8, 6))
sns.heatmap(correlation_matrix, annot=True, cmap='coolwarm', fmt='.2f')
plt.title('Pairwise Correlation of Winning Numbers')
plt.show()